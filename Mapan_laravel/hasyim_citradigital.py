def apply_blending(b):
    global img_original
    with output_blend:
        clear_output()
        if img_original is None:
            print("Silakan unggah gambar di sel pertama!")
            return

        # Buat gambar kedua (gradient warna) dengan ukuran yang sama
        h, w, c = img_original.shape
        img2 = np.zeros((h, w, c), np.uint8)
        img2[:] = (255, 100, 0) # Warna solid sebagai contoh

        alpha = blend_slider.value
        beta = 1.0 - alpha
        dst = cv.addWeighted(img_original, alpha, img2, beta, 0.0)

        print("Kiri: Asli | Kanan: Hasil Gabungan dengan Warna Solid")
        cv2_imshow(np.hstack((img_original, dst)))

blend_btn.on_click(apply_blending)
display(blend_slider, blend_btn, output_blend)

