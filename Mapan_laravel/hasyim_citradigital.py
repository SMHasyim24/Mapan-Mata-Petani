def proses_gambar(b):
    global img_original
    with output_dash:
        clear_output()
        if img_original is None:
            print("Silakan unggah gambar di sel paling atas terlebih dahulu!")
            return

        res = None
        if menu_opsi.value == 1:
            res = cv.convertScaleAbs(img_original, alpha=contrast_slider.value, beta=bright_slider.value)
            print("Hasil Brightness & Contrast:")
        elif menu_opsi.value == 2:
            img2 = np.zeros_like(img_original)
            img2[:] = (255, 150, 0)
            res = cv.addWeighted(img_original, blend_slider.value, img2, 1 - blend_slider.value, 0)
            print("Hasil Blending dengan Warna:")
        elif menu_opsi.value == 3:
            gray = cv.cvtColor(img_original, cv.COLOR_BGR2GRAY)
            res = cv.cvtColor(gray, cv.COLOR_GRAY2BGR)
            print("Hasil Grayscale:")
        elif menu_opsi.value == 4:
            gray = cv.cvtColor(img_original, cv.COLOR_BGR2GRAY)
            _, thresh = cv.threshold(gray, thresh_slider.value, 255, cv.THRESH_BINARY)
            res = cv.cvtColor(thresh, cv.COLOR_GRAY2BGR)
            print(f"Hasil Thresholding (Nilai: {thresh_slider.value}):")
        elif menu_opsi.value == 5:
            res = cv.bitwise_not(img_original)
            print("Hasil Negasi (Citra Negatif):")
        else:
            print("Pilih operasi terlebih dahulu.")
            return

        if res is not None:
            cv2_imshow(np.hstack((img_original, res)))

