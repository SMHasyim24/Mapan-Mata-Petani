import cv2
import numpy as np
from google.colab.patches import cv2_imshow

#Membaca Gambar
img = cv2.imread('/content/photo_2026-06-04_09-34-50.jpg')

#OpenCV menggunakan BGR
B = img[:, :, 0].astype(float)
G = img[:, :, 1].astype(float)
R = img[:, :, 2].astype(float)

#RGB -> CMY
C = 255 - R
M = 255 - G
Y = 255 - B

#Menentukan K
K = np.minimum(np.minimum(C, M), Y)

#Mengurangi K dari C, M, Y
C = C - K
M = M - K
Y = Y - K

#Mengembalikan CMYK menjadi RGB
C2 = C + K
M2 = M + K
Y2 = Y + K

#Pastikan tidak melebihi 255
C2 = np.clip(C2, 0, 255)
M2 = np.clip(M2, 0, 255)
Y2  = np.clip(Y2, 0, 255)

R2 = 255 - C2
G2 = 255 - M2
B2 = 255 - Y2

#Gabungkan kembali
rgb_hasil = cv2.merge([
    B2.astype(np.uint8),
    G2.astype(np.uint8),
    R2.astype(np.uint8)
])

print("RGB ke CMYK")
cv2_imshow(rgb_hasil)

