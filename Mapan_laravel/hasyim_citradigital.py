def apply_brightness_contrast(b):
    global img_original, img_processed
    with output_bc:
        clear_output()
        if img_original is None:
            print("Silakan unggah gambar di sel pertama terlebih dahulu!")
            return

        # Rumus: g(x) = alpha * f(x) + beta
        alpha = contrast_slider.value
        beta = bright_slider.value
        img_processed = cv.convertScaleAbs(img_original, alpha=alpha, beta=beta)

        print(f"Hasil (Contrast: {alpha}, Brightness: {beta}):")
        # Menampilkan Sebelum dan Sesudah
        combined = np.hstack((img_original, img_processed))
        cv2_imshow(combined)

apply_btn.on_click(apply_brightness_contrast)
